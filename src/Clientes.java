    private void btmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmodificarActionPerformed
         conectar c = new conectar();
         Connection cn = c.conexion();
            
        try{
            String sql = "UPDATE usuario SET nombre=?, apepat=?, apemat=?, genero=?, telefono=?, correo=?, direccion=? " +
                    "WHERE idcte=?";
            int fila = tdatos.getSelectedRow();
            String dato =(String) tdatos.getValueAt(fila, 0);
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, txtnombre.getText());
            pst.setString(2, txtapepat.getText());
            pst.setString(3, txtapemat.getText());
            pst.setString(4, cbgenero.getSelectedItem().toString());
            pst.setString(5, txttel.getText());
            pst.setString(6, txtcorreo.getText());
            pst.setString(7, txtdireccion.getText());
            pst.setString(8, dato);
            int n = pst.executeUpdate();
            if(n>0){
               
               cargar("");
               limpiar();
               JOptionPane.showMessageDialog(null, "Datos Modificados");
            }
        } catch (Exception e){
                JOptionPane.showMessageDialog(null, "Error" + e.getMessage());
            
                    
                
            }
    }//GEN-LAST:event_btmodificarActionPerformed

