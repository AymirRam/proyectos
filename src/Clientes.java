    private void btguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btguardarActionPerformed
        
        conectar c = new conectar();
            Connection cn = c.conexion();
            
            String sql = "";
            nombre = txtnombre.getText();
            paterno = txtapepat.getText();
            materno = txtapemat.getText();
            genero = cbgenero.getSelectedItem().toString();
            telefono = txttel.getText();
            correo = txtcorreo.getText();
            direccion = txtdireccion.getText();
            sql= "INSERT INTO usuario(nombre, apepat, apemat, genero, telefono,correo, direccion) VALUES (?, ?, ?, ?,?, ?, ?)";
            try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1,nombre);
            pst.setString(2, paterno);
            pst.setString(3, materno);
            pst.setString(4,genero);
            pst.setString(5,telefono);
            pst.setString(6,correo);
            pst.setString(7,direccion);
            int n = pst.executeUpdate();
            if(n>0)
               JOptionPane.showMessageDialog(null, "Registro guardado exitosamente");
            desbloquear();
            cargar("");
            
        } catch (SQLException ex) {
            Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    
    }//GEN-LAST:event_btguardarActionPerformed

