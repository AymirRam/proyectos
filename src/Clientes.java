    //evento para cargar los registros guardados en la tabla 
    void cargar(String valor){
        
        String [] titulos ={"idcte","nombre","apepat","apemat","genero","telefono","correo","direccion"}; 
        String [] registros = new String[8];
        String sql = "SELECT * FROM usuario WHERE CONCAT(nombre) like '%"+valor+"%'";
        model = new DefaultTableModel(null, titulos);
        
        
        conectar cc = new conectar();
        Connection cn =cc.conexion();
        
        try {
            Statement  st =cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                registros[0]=rs.getString("idcte");
                registros[1]=rs.getString("nombre");
                registros[2]=rs.getString("apepat");
                registros[3]=rs.getString("apemat");
                registros[4]=rs.getString("genero");
                registros[5]=rs.getString("telefono");
                registros[6]=rs.getString("correo");
                registros[7]=rs.getString("direccion");
                
                model.addRow(registros);
            
                tdatos.setModel(model);
                
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    
    } 
    
