    void cargar(String valor){
        
        String [] titulos ={"idproducto","nombre","codprod","categoria","marca","caracteristicas","proveedor","preciocom","preciovt","cantidad"}; 
        String [] registros = new String[10];
        String sql = "SELECT * FROM productos WHERE CONCAT(nombre) like '%"+valor+"%'";
        model = new DefaultTableModel(null, titulos);
        
        
        conectar cc = new conectar();
        Connection cn =cc.conexion();
        
        try {
            Statement  st =cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                registros[0]=rs.getString("idproducto");
                registros[1]=rs.getString("nombre");
                registros[2]=rs.getString("codprod");
                registros[3]=rs.getString("categoria");
                registros[4]=rs.getString("marca");
                registros[5]=rs.getString("caracteristicas");
                registros[6]=rs.getString("proveedor");
                registros[7]=rs.getString("preciocom");
                registros[8]=rs.getString("preciovt");
                registros[9]=rs.getString("cantidad");               
                model.addRow(registros);
            
                tdatosprod.setModel(model);
                
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    
    } 
    
