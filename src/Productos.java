    private void btguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btguardarActionPerformed
            conectar c = new conectar();
            Connection cn = c.conexion();
            
            String sql = "";
            nombre = txtproducto.getText();
            codprod = txtcodprod.getText();
            categoria = cbcategoria.getSelectedItem().toString();
            marca = txtmarca.getText();
            caract = txtcaracteristicas.getText();
            proveedor = txtproveedor.getText();
            preciocom = txtpreciocom.getText();
            preciovt = txtpreciovt.getText();
            cantidad = txtcantidad.getText();
            
            sql= "INSERT INTO productos(nombre, codprod, categoria, marca, caracteristicas,proveedor, preciocom, preciovt, cantidad) VALUES (?, ?, ?, ?,?, ?, ?, ?, ?)";
            try {
             
                PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, nombre);
            pst.setString(2, codprod);
            pst.setString(3, categoria);
            pst.setString(4, marca);
            pst.setString(5, caract);
            pst.setString(6, proveedor);
            pst.setString(7, preciocom);
            pst.setString(8, preciovt);
            pst.setString(9, cantidad);
            
            int n = pst.executeUpdate();
            if(n>0)
               JOptionPane.showMessageDialog(null, "Registro guardado exitosamente");
            desbloquear();
            cargar("");
            
        } catch (SQLException ex) {
            Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    
    }//GEN-LAST:event_btguardarActionPerformed

